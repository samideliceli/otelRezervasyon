            String sql = "INSERT INTO GOREV (AD,MAAS,IZIN_HAKKI) VALUES (?,?,?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, gorev);
            ps.setInt(2, Integer.parseInt(maas));

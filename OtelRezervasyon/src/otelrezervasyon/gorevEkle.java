    private boolean textDoluMU(){
        gorev = jTextFieldGorevAdi.getText();
        maas = jTextFieldMaas.getText();
        izinHakki = jTextFieldIzinHakki.getText();
        if(!(gorev.equals("")||maas.equals("")||izinHakki.equals(""))){
            return true;
        }
        else{
            return false;
        }
        
    }


